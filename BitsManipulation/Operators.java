package BitsManipulation;

public class Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;
        // Binary AND
        System.out.println(a & b);
        // Binary OR
        System.out.println(a | b);
        // Binary NOT
        System.out.println(~a);
        // Binay Left Shif
        System.out.println(a << 2);
        // Binary Right Shift
        System.out.println(b >> 2);
        // Binary XOR
        System.out.println(a ^ b);
    }
}
